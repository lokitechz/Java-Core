package vn.example.bitejava.constant;

public class Constant {

    public static void main(String[] args) {
        // Bắt buộc sử dụng từ khoá final
        // Tên của hằng số luôn viết hoa toàn bộ
        final float PI = 3.14f;
        System.out.println("Giá trị của hằng số PI là: " + PI);
    }

}
